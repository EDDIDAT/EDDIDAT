        fprintf(fid,['LNr\t',...
            'Emax\t',...
            'dEmax\t',...
            'Iint\t',...
            'Ib\t',...
            'FWHM\t',...
            'FWHM_Gauss\t',...
            'FWHM_Lorentz\t',...
            'tth\t',...
            'phiP\t',...
            'psiP\t',...
            'etaP\t',...
            'h\t',...
            'k\t',...
            'l\t',...
            'xdiff\t',...
            'ydiff\t',...
            'zdiff\t',...
            '\n\n']);
    end
    
%% (* Daten eintragen *)
    %--> Durchlaufen aller Peaks
    for i_c = 1:size(DL,1)
        if SpecFileCheck == 1
            % Peak-Infos
            fprintf(fid,'%d.\t%.3f\t%.4f\t%.2f\t%.4f\t%.4f\t%.4f\t%.2e\t%.2f\t%.2f\t%.2f\t%.2f\t%d\t%d\t%d\t%.1f\t%.2f\t%.2f\t%.4f\t%.4f\t%.4f\t%.4f\t%.4f\t%.4f\t%.1f\t%.1f%.2f\t%s\n',...
                DL(i_c,1:23),datestr(DL(i_c,24),'dd-mm-yyyy-HH-MM-SS'));
        else
            % Peak-Infos
            fprintf(fid,'%d.\t%.3f\t%.4f\t%.2f\t%.4f\t%.4f\t%.4f\t%.2e\t%.2f\t%.2f\t%.2f\t%.2f\t%d\t%d\t%d\t%.1f\t%.2f\t%.2f\t%.4f\t%.4f\t%.4f\t%.4f\n',...
                DL(i_c,1:18));
        end
    end    
end
    %Datei schließen
    fclose(fid);
end